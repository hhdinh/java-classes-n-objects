package javaClassesObjects;

public class Pet {
	private String name;
	private int age;
	private String location;
	private String type;

	public Pet() {
	}

	public Pet(String name, int age, String location, String type) {
		this.name = name;
		this.age = age;
		this.location = location;
		this.type = type;
	}
}
